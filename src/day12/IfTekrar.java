package day12;

public class IfTekrar {
    public static void main(String[] args) {
        int k1 =4;
        int k2 =8;

        if (k1>k2){
            System.out.println("k1 büyüktür.");
        }
        if (k1==k2){
            System.out.println("k1 ve k2 eşitiir.");
        }
        if (k1<k2){
            System.out.println("k1 küçüktür.");
        }
    }
}
