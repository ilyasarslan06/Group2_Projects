package day11;

public class KarsilastirmaOperatorleri {
    public static void main(String[] args) {
        /*
        Relation Operators - İlişkisel (Karşılaştırma) Operatörler

        == --> Eşit MİDİR ?
        != --> Eşit değil Midir?
        <
        >
        <=
        >=
         */

        int x=5;
        int y=7;
        // ctrl + alt +V KULLAN Kİ METOT OTOMATİK GELSİN.

        boolean b = x == y;
        boolean b1 = 5 > 3;

        boolean b2 = x!=y;
        boolean b3 = 7 < 4;
        boolean b4 = x >= y;
        boolean b5 = x <= y;

        System.out.println("b --> "+b);
        System.out.println("b1 --> "+b1);
        System.out.println("b2 --> "+b2);
        System.out.println("b4 --> "+b4);
        System.out.println("b5 --> "+b5);




    }
}
